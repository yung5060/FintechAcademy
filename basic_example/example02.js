function plus(p1, p2) {
    return p1 + p2;
}

console.log(plus(3, 9));

// Going Forward we will use only the type of code from below when making a function

const arrowPlus = (p1, p2) => {
    return p1 + p2;
}

console.log(arrowPlus(3, 9));

const arrowMinus = (p1, p2) => {
    return p1 - p2;
}
console.log(arrowMinus(9, 3));

const arrowDivision = (p1, p2) => {
    return p1 / p2;
}
console.log(arrowDivision(5, 2));

const arrowMultiplication = (p1, p2) => {
    return p1 * p2;
}
console.log(arrowMultiplication(3, 9));